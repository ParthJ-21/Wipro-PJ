package com.wipro.oop;

public class Converter {
	public int convert(int a) {
        return a * a;  // Square of a
    }

   
    public int convert(int a, int b) {
        return a + b;  // Sum of a and b
    }

   
    public double convert(double a) {
        return Math.abs(a);
    }
}

